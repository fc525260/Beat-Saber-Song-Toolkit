import 'package:beat_saber_song_toolkit/beat_saber_song_toolkit.dart';

String mapTitleForTest(BeatSaverMap map) {
  return map.metadata.songName.isEmpty ? map.name : map.metadata.songName;
}

bool matchesSearchRegexForTest(BeatSaverMap map, RegExp regex) {
  final haystack = [
    map.id,
    map.name,
    map.metadata.songName,
    map.metadata.songSubName,
    map.metadata.songAuthorName,
    map.metadata.levelAuthorName,
    map.uploaderName ?? '',
    ...map.tags,
  ].join('\n');
  return regex.hasMatch(haystack);
}

bool matchesFieldFilterTokensForTest(
  BeatSaverMap map,
  Set<String> tokens, {
  required bool title,
  required bool songName,
  required bool songAuthor,
  required bool mapper,
  required bool description,
  required bool tags,
}) {
  final haystack = fieldFilterHaystackForTest(
    map,
    title: title,
    songName: songName,
    songAuthor: songAuthor,
    mapper: mapper,
    description: description,
    tags: tags,
  ).toLowerCase();
  return tokens.every(haystack.contains);
}

bool matchesFieldFilterRegexForTest(
  BeatSaverMap map,
  RegExp regex, {
  required bool title,
  required bool songName,
  required bool songAuthor,
  required bool mapper,
  required bool description,
  required bool tags,
}) {
  return regex.hasMatch(
    fieldFilterHaystackForTest(
      map,
      title: title,
      songName: songName,
      songAuthor: songAuthor,
      mapper: mapper,
      description: description,
      tags: tags,
    ),
  );
}

String fieldFilterHaystackForTest(
  BeatSaverMap map, {
  required bool title,
  required bool songName,
  required bool songAuthor,
  required bool mapper,
  required bool description,
  required bool tags,
}) {
  final useAll =
      !title && !songName && !songAuthor && !mapper && !description && !tags;
  final fields = <String>[
    if (useAll || title) map.name,
    if (useAll || songName) ...[
      map.metadata.songName,
      map.metadata.songSubName,
    ],
    if (useAll || songAuthor) map.metadata.songAuthorName,
    if (useAll || mapper) ...[
      map.metadata.levelAuthorName,
      map.uploaderName ?? '',
    ],
    if (useAll || description) map.description,
    if (useAll || tags) ...map.tags,
  ];
  return fields.join('\n');
}

bool matchesChinesePresetForTest(BeatSaverMap map) {
  final haystack = [
    map.name,
    map.description,
    map.metadata.songName,
    map.metadata.songSubName,
    map.metadata.songAuthorName,
    map.metadata.levelAuthorName,
    map.uploaderName ?? '',
  ].join('\n');
  if (RegExp(r'[\u3400-\u9fff]').hasMatch(haystack)) {
    return true;
  }
  final syllables = RegExp(r'[a-zA-Z]+')
      .allMatches(haystack)
      .map((match) => match.group(0)!.toLowerCase())
      .where(_isLikelyPinyinSyllable)
      .toSet();
  return syllables.length >= 2 ||
      syllables.any((syllable) => syllable.length >= 5);
}

bool _isLikelyPinyinSyllable(String value) {
  return _commonPinyinSyllables.contains(value);
}

